// pergunte ao usuuário qual o dia da semana. Se a resposta for sabado ou domindo
// mostre "Bom fim de semana". Caso contrário, "Boa semana"
let diaDaSemana = prompt("Qual o dia da semana?");

if(diaDaSemana === "sabado" || diaDaSemana === "domingo") {
    alert("Bom final de semanda");
} else {
    alert("Boa semana");
}
let inputNumero = prompt("Digite um número");
if(inputNumero < 0 ){
    alert("O numero informado é negativo");
} else {
    alert("O numero informado é positivo");
}
